    <h2 id="titleHistoria">NOSSA HISTORIA</h2>

    <p id="textHistoria">O StudioRV nasceu há 7 anos, de uma percepção simples: o treino livre na musculação era ineficaz sem orientação. O mercado oferecia máquinas, mas não resultados garantidos.

    O Começo foi criar um espaço que invertesse essa lógica: oferecer o melhor acesso aos equipamentos, mas com a condição de um acompanhamento rigoroso.

    O Meio foi aprimorar nossa metodologia, unindo treino e dieta em uma consultoria única e 100% individualizada. Transformamos a ficha genérica em um plano de evolução constante, feito por profissionais.

    Hoje, somos a referência para quem busca resultados reais. Nossa história é a prova de que a disciplina do aluno, combinada com a precisão da nossa consultoria, é a fórmula imbatível para o sucesso.</p>
